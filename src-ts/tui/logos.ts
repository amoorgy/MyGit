const LOGO_TINY = ["MY GIT"];

// ============================================================================
// LOGO REGISTRY
// ============================================================================

interface LogoVariant {
    large: string[];
    compact: string[];
}

const LOGOS: Record<LogoFont, LogoVariant> = {
    block: { large: BLOCK_LARGE, compact: BLOCK_COMPACT },
    simple3d: { large: SIMPLE3D_LARGE, compact: SIMPLE3D_COMPACT },
    threeD: { large: THREED_LARGE, compact: THREED_COMPACT },
};

function logoWidth(lines: string[]): number {
    return Math.max(...lines.map((l) => l.length), 0);
}

/**
 * Font fallback priority chain (like the Rust version).
 */
function fontPriority(preferred: LogoFont): LogoFont[] {
    switch (preferred) {
        case "block":
            return ["block", "simple3d", "threeD"];
        case "simple3d":
            return ["simple3d", "block", "threeD"];
        case "threeD":
            return ["threeD", "simple3d", "block"];
    }
}

/**
 * Select the best logo that fits the given terminal width.
 * Tries: preferred font large вҶ’ compact вҶ’ next font large вҶ’ compact вҶ’ ... вҶ’ TINY.
 */
export function selectLogo(width: number, preferredFont: LogoFont): string[] {
    const fonts = fontPriority(preferredFont);

    for (const font of fonts) {
        const variant = LOGOS[font];
        if (logoWidth(variant.large) <= width) return variant.large;
        if (logoWidth(variant.compact) <= width) return variant.compact;
    }

    return LOGO_TINY;
}
